ASSUME CS:CODE,DS:DATA
DATA SEGMENT
LIST DB 52H,23H,56H,45H,77H
COUNT EQU 05H
MAX DB ?
DATA ENDS

CODE SEGMENT
START:MOV AX,DATA
	  MOV DS,AX
	  LEA SI,LIST
	  MOV CL,COUNT-1
	  MOV AL,[SI]

AGAIN:CMP AL,[SI+1]
	  JNL NEXT
	  MOV AL,[SI+1]

NEXT :INC SI
	  DEC CL
	  JNZ AGAIN
	  LEA SI,MAX
	  MOV [SI],AL
	  
MOV AH,4CH
INT 21H
CODE ENDS
END START